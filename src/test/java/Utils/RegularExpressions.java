package Utils;

public class RegularExpressions {
    /**
     . --> one instance of any character

     * --> zero or more instances of character before it

     + --> one or more instances of character before it

     .* --> zero or more occurrences of anything

     .+ --> one or more occurrences of anything

     \d --> only numeric data (one digit between 0 to 9)

     \d+ --> one or more occurrences of numeric data
     */
}
